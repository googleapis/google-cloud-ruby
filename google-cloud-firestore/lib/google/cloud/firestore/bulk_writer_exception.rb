module Google
  module Cloud
    module Firestore
      class BulkWriterException < StandardError
        attr_reader :status
        attr_reader :message
        attr_reader :details

        def initialize status
          @status = status.code
          @message = status.message
          @details = status.details

          super status.message
        end
      end
    end
  end
end
